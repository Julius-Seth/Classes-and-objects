 class Multipleattributes {
  String fname = "Julius";
  String lname = "Seth";
  int age = 18;

  public static void main(String[] args) {
    Multipleattributes myObj = new Multipleattributes();
    System.out.println("Name: " + myObj.fname + " " + myObj.lname);
    System.out.println("Age: " + myObj.age);
  }
}